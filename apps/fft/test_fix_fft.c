int absolute(int number) {
  if (number < 0)
    return (0 - number);
  else 
    return number;  
}

int main(){
  // Initializing the Safe Stack Pointer
  SSPL = 0x68;
  SSPH = 0x09;

  // Initialize memory
  mem_init();

  dom0_main();
  while(1);
  return 0;
}

int dom0_realmain()
{
  int i, scale;
  unsigned* diff;
  short *x, *fx;
  x = mmc_malloc(sizeof(short) * N);
  fx = mmc_malloc(sizeof(short) * N);
  diff = mmc_malloc(sizeof(unsigned));

  DDRA = 0xFF;
  PORTA = 0x00;

  for (i=0; i<N; i++){
    x[i] = pgm_read_word_near((short*)&cosine[i]);
    if (i & 0x01)
      fx[(N+i)>>1] = x[i];
    else
      fx[i>>1] = x[i];
  }

  PORTA = 0x01;

  mmc_change_own((void*)fx, 1);
  fix_fftr(fx, log2N, 0);

  PORTA = 0x02;

  scale = fix_fftr(fx, log2N, 1);

  PORTA = 0x03;

  for (i=0,*diff=0; i<N; i++) {
    int sample;
    if (i & 0x01)
      sample = fx[(N+i)>>1] << scale;
    else
      sample = fx[i>>1] << scale;
    *diff += absolute(x[i]-sample);
  }

  PORTA = 0x04;

  PORTA = (uint8_t)(*diff);
  PORTA = (uint8_t)(*diff >> 8);

  PORTA = 0x05;

  while(1);

  return 0;
}

